#include "Container.h"

/****************************************************************
* Overview: Basic constructor and destructor
*****************************************************************/
Container::Container(){}
Container::~Container(){}

/****************************************************************
* Overview: insertion operator for Container objects
*
*    Input: stream to write to (s), and container to display (a)
*   Output: ostream& of edited stream
*****************************************************************/
ostream& operator<<(ostream& s, Container const& a){
	return a.Print(s);
}